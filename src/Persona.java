
public class Persona {
	
	
	String nombre;
	String correo;
	String pedido;
	
		public Persona(String nombre, String correo, String pedido) {
		super();
		this.nombre = nombre;
		this.correo = correo;
		this.pedido = pedido;
		}

		@Override
		public String toString() {
			return "Persona [nombre=" + nombre + ", correo=" + correo + ", pedido=" + pedido + "]";
		}




		














}
